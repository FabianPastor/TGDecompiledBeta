package org.telegram.ui.Components;

public class Rect {
    public float height;
    public float width;
    public float x;
    public float y;

    public Rect() {
    }

    public Rect(float x2, float y2, float width2, float height2) {
        this.x = x2;
        this.y = y2;
        this.width = width2;
        this.height = height2;
    }
}
