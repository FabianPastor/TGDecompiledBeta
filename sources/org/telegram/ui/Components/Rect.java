package org.telegram.ui.Components;

public class Rect {
    public float height;
    public float width;
    public float x;
    public float y;

    public Rect() {
    }

    public Rect(float f, float f2, float f3, float f4) {
        this.x = f;
        this.y = f2;
        this.width = f3;
        this.height = f4;
    }
}
